package main

import (
	"testing"
)

var sample = `0 3 6 9 12 15
1 3 6 10 15 21
10 13 16 21 30 45`

func Test_part1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "sample",
			input: sample,
			want:  114,
		},
		{
			name:  "actual",
			input: input,
			want:  1995001648,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

